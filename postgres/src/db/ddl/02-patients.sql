CREATE TABLE IF NOT exists hospital.PATIENTS(
    ID VARCHAR(255) PRIMARY KEY,
    BIRTHDATE TIMESTAMP  NOT NULL,
    DEATHDATE TIMESTAMP,
    SSN VARCHAR(255),
    DRIVERS VARCHAR(255),
    PASSPORT VARCHAR(255),
    PREFIX VARCHAR(255),
    FIRST VARCHAR(255)  NOT NULL,
    LAST VARCHAR(255)  NOT NULL,
    SUFFIX VARCHAR(255),
    MAIDEN VARCHAR(255),
    MARITAL VARCHAR(255),
    RACE VARCHAR(255),
    ETHNICITY VARCHAR(255),
    GENDER VARCHAR(2)  NOT NULL,
    BIRTHPLACE VARCHAR(255),
    ADDRESS TEXT,
    CITY VARCHAR(255),
    STATE VARCHAR(255),
    County VARCHAR(255),
    FIPS_County_Code VARCHAR(255),
    ZIP VARCHAR(100),
    Lat VARCHAR(255),
    Lon VARCHAR(255),
    Healthcare_Expenses VARCHAR(255),
    Healthcare_Coverage VARCHAR(255),
    Income VARCHAR(255)
);
